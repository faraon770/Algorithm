# Задача 7: Проверка палиндрома (рекурсивно)
def is_palindrome(text):
    """Рекурсивно проверяет, является ли строка палиндромом."""
    text = text.lower().replace(" ", "")
    if len(text) <= 1:
        return True
    if text[0] != text[-1]:
        return False
    return is_palindrome(text[1:-1])

