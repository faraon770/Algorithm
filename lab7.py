def add(a, b):
    """Возвращает сумму двух чисел."""
    return a + b

def power(a, n=2):
    """Возводит число в степень (по умолчанию в квадрат)."""
    return a ** n

def sum_all(*args):
    """Возвращает сумму произвольного количества чисел."""
    total = 0
    for value in args:
        total += value
    return total

