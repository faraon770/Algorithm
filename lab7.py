depth_counter = 0

def count_depth(n):
    """Рекурсивно уменьшает число до 0 и возвращает количество вызовов."""
    global depth_counter
    if n == 0:
        return 0
    depth_counter += 1
    return 1 + count_depth(n - 1)

depth_counter = 0
