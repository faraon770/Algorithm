# Задача 9: Быстрое возведение в степень (рекурсивно)
def fast_power(a, n):
    """Рекурсивное быстрое возведение в степень."""
    if n == 0:
        return 1
    if n == 1:
        return a
    if n % 2 == 0:
        half = fast_power(a, n // 2)
        return half * half
    else:
        return a * fast_power(a, n - 1)

print(f"Задача 9 - fast_power(2, 10) = {fast_power(2, 10)}")

